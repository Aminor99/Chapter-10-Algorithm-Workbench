public abstract class B {
   
   private int m;
   protected int n;
   
   public void setM (int M){
      m = M;
   }
   public int getM (){
      return m;
   }
   public void setN (int N){
      n = N;
   }
   public int getN (){
      return n;
   }
   public abstract double calc();
   
}